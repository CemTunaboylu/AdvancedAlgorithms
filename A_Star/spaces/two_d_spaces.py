def form_giant(x=50, y=50):
    rows  = {}
    rows[0] = [0]*x
    rows[1] = [1]*(x-1)+[0]
    rows[2] = [0]*x
    rows[3] = [0]+[1]*(x-1)

    mat = [None]*y
    for i in range(y):
        mat[i] = rows[i%4]
    return mat


